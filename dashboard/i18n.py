TRANSLATIONS = {"en": EN, "zh": ZH}


def t(key: str, locale: str | None = None) -> str:
    """Return translated string for key. locale defaults to session_state or 'en'."""
    if locale is None:
        try:
            import streamlit as st
            locale = st.session_state.get("locale", "en")
        except Exception:
            locale = "en"
    return TRANSLATIONS.get(locale, EN).get(key, key)
